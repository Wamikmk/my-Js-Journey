const month = 3

// Switch is an alternative for checking conditions which otherwise would require many if else statements.

switch (month) {
    case 1:
        console.log("Jan")
        
        break;
    case 2:
        console.log("Feb")
        
        break;
    case 3:
        console.log("march")
        
        break;
    case 4:
        console.log("april")
        
        break;

    default:
        break;
}